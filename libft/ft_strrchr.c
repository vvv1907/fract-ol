#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;
	char	*ptr;

	len = ft_strlen(s);
	ptr = (char *)s;
	if (c == 0)
		return (&ptr[len]);
	while (len--)
		if (ptr[len] == (unsigned char)c)
			return (&ptr[len]);
	return (NULL);
}
