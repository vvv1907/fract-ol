#include "libft.h"

static char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	while (len--)
		*dst++ = *src++;
	*dst = '\0';
	return (dst);
}

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	len;

	len = ft_strlen(src);
	if (dstsize == 0)
		return (len);
	if (len < dstsize)
		ft_strncpy(dst, src, len);
	else
		ft_strncpy(dst, src, dstsize - 1);
	return (len);
}
